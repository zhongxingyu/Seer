     private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         int returnVal = jFileChooser1.showOpenDialog(this);
         if (returnVal != JFileChooser.APPROVE_OPTION)
         {
             return;
         }
         selectedFiles = jFileChooser1.getSelectedFiles();
         String filesStr = "";
         for (int i = 0; i < selectedFiles.length; i++)
         {
             filesStr += "\"" + selectedFiles[i].getAbsolutePath() + "\" ";
         }
         jTextField1.setText(filesStr);
     }//GEN-LAST:event_jButton1ActionPerformed
 
     private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         if (jTextField1.getText().isEmpty())
         {
             JOptionPane.showMessageDialog(this,"Please choose a file first.");
             jTextField1.selectAll();
             return;
         }
         
         List<String> list = new ArrayList<String>(selectedFiles.length);
         for (int i = 0; i < selectedFiles.length; i++)
         {
             list.add("\"" + selectedFiles[i].getAbsolutePath() + "\"");
         }
 
         IDAL dataAccessLayer = Utils.getDataAccessLayer();
         Importer importer = new Importer(dataAccessLayer, list);
         Iterator<XmcdDisc> iterator = importer.getDiscsToImportIterator();
         
         while (iterator.hasNext())
         {
             XmcdDisc disc = iterator.next();
             try {
                 importer.importDisc(disc);
             } catch (IOException ex) {
                 Logger.getLogger(Import.class.getName()).log(Level.SEVERE, null, ex);
             } catch (DALException ex) {
                 Logger.getLogger(Import.class.getName()).log(Level.SEVERE, null, ex);
             }
         }
        
        importer.closeImporter();
     }//GEN-LAST:event_jButton2ActionPerformed
 
