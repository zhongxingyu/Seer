     private class myThread1 implements Runnable {
 
         @Override
         public void run() {
             try {
                 XmcdDiscArchives archives = new XmcdDiscArchives(Arrays.asList(selectedFiles), true);
                 Iterator<XmcdDisc> toImport = new XmcdFilters.Factory(archives).defaultFilters().chain();
 
                 SetStatusSafely("Preparing for import...");
                 Database.getDataAccessLayer().prepareDbPreImport();
 
                 SetStatusSafely("Import in progress...");
                 importer.importFreedb(toImport);
                 archives.close();
 
                 SetStatusSafely("Finalizing import...");
                 Database.getDataAccessLayer().prepareDbPostImport();
 
                 SetFinishedSafely();
             } catch (DALException ex) {
                SetStatusSafely("Database error! Please restart the application.");
             } catch (IOException ex) {
                 SetStatusSafely("Error! " + ex.getCause().getMessage());
             } finally {
                 // Allow the user to close the dialog
                 SetCanCloseSafely();
             }
         }
     }
 
     private void SetProgress(long importedCount, long parsedCount) {
         jLabelImportedCount.setText(String.valueOf(importedCount));
         jLabelParsedCount.setText(String.valueOf(parsedCount));
     }
 
     private void SetProgressSafely(long importedCount, long parsedCount) {
         final long _importedCount = importedCount;
         final long _parsedCount = parsedCount;
 
         Runnable r = new Runnable() {
 
             @Override
             public void run() {
                 try {
                     SetProgress(_importedCount, _parsedCount);
                 } catch (Exception ex) {
                     logger.log(Level.SEVERE, null, ex);
                 }
             }
         };
 
         SwingUtilities.invokeLater(r);
     }
 
     private class myThread2 implements Runnable {
 
         @Override
         public void run() {
             long importedCount;
             long parsedCount;
 
             while (!isCurrentImportDone) {
                 importedCount = importer.getFilesImportedCount();
                 parsedCount = importer.getFilesParsedCount();
 
                 SetProgressSafely(importedCount, parsedCount);
 
                 try {
                     Thread.sleep(500);
                 } catch (InterruptedException ex) {
                     logger.log(Level.SEVERE, null, ex);
                 }
             }
         }
     }
 
