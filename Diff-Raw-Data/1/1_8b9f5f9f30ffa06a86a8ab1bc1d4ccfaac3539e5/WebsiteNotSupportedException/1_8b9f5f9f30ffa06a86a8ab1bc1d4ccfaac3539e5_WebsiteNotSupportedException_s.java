
 public class WebsiteNotSupportedException extends Exception {
 
 	public WebsiteNotSupportedException() { super(); }
 	public WebsiteNotSupportedException(String message) { super(message); }
 	public WebsiteNotSupportedException(String message, Throwable cause) { super(message, cause); }
 	public WebsiteNotSupportedException(Throwable cause) { super(cause); }
 }
 
 
