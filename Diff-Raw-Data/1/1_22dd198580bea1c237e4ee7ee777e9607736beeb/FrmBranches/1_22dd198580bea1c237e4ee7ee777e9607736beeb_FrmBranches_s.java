     @Override
     public boolean saveRegister() {
         try {
             int id = 0;
             id = !txtId.getText().equals("") ? Integer.valueOf(txtId.getText()) : 0;
             id = controller.saveRecord(id, txtCpfCnpj.getText(), txtCorporateName.getText(),
                     txtCep.getText(), txtCity.getText(), cboUf.getSelectedItem().toString(),
                     txtAddress.getText(), txtAddressNumber.getText(), txtDistrict.getText(),
                     txtPhone.getText(), txtEmail.getText(), chkActive.isSelected());
             txtId.setText(String.valueOf(id));
             Utils.showInfoMessage(this, SAVE_SUCCESS);
             resetList();
             return true;
         } catch (Exception e) {
             Logger.getLogger(FrmBranches.class.getName()).log(Level.SEVERE, null, e);
             Utils.showErrorMessage(this, SAVE_ERROR);
         }
         return false;
     }
 
     @Override
     public boolean deleteRegister() {
         if(!Utils.showQuestionMessage(this, DELETE_CONFIRMATION)) { return false; }
         try {
             controller.inative(Integer.valueOf(txtId.getText()));
             newRegister();
             resetList();
             return true;
         } catch (Exception ex) {
             Logger.getLogger(FrmBranches.class.getName()).log(Level.SEVERE, null, ex);
             Utils.showErrorMessage(this, DELETE_ERROR);
             return false;
         }
     }
     
     @Override
     public void getValues(Branches register) {
         txtId.setText(String.valueOf(register.getId()));
         txtAddress.setText(register.getAddress().getAddress());
         txtAddressNumber.setText(register.getAddress().getAddressNumber());
         txtCep.setText(register.getAddress().getCep());
         txtCity.setText(register.getAddress().getCity());
         txtCpfCnpj.setText(register.getCnpj());
         txtDistrict.setText(register.getAddress().getDistrict());
         txtEmail.setText(register.getEmail());
         txtCorporateName.setText(register.getName());
         txtPhone.setText(register.getPhone());
         chkActive.setSelected(register.isActive());
         controller.setNewRecord(false);
     }
     
     public final void resetList() {
         try {
             this.setList(controller.list());
             newRegister();
         } catch (Exception ex) {
             Logger.getLogger(FrmBranches.class.getName()).log(Level.SEVERE, null, ex);
         }
     }
 
     @Override
     public void searchRegister() {
         FrmCustomerSearch search = new FrmCustomerSearch();
         search.show();
         try {
             getValues(controller.findById((Integer)search.getReturnId()));
         } catch (Exception ex) {
             Logger.getLogger(FrmBranches.class.getName()).log(Level.SEVERE, null, ex);
         }
     }
 
     @Override
     public void setNewRegisterInController() {
         controller.setNewRecord(true);
     }
 
     /*
      * Não é igual para todas as telas
      */
     private void initComboBox() {
         cboUf.removeAllItems();
         for(String uf : Utils.getAbbreviationStates()) {
             cboUf.addItem(uf);
         }
     }
     
     private void setCepValues(String cep) {
         Address address;
         try {
             address = new AddressController().getAddressFromCep(cep);
             txtAddress.setText(address.getAddress());
            txtAddressNumber.setText(address.getAddressNumber());
             txtCity.setText(address.getCity());
             txtDistrict.setText(address.getDistrict());
             cboUf.setSelectedItem(address.getUf());
         } catch (Exception ex) {
             Logger.getLogger(FrmCustomers.class.getName()).log(Level.SEVERE, null, ex);
         }
     }
