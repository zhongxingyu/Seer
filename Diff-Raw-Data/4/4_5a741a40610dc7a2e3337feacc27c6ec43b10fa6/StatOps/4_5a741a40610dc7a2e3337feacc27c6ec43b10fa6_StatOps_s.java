     private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         try {
             String fromYear1 = (String) jComboBox6.getSelectedItem();
             String fromMonth1 = (String) jComboBox5.getSelectedItem();
             String fromDay1 = (String) jComboBox4.getSelectedItem();
 
             String toYear1 = (String) jComboBox2.getSelectedItem();
             String toMonth1 = (String) jComboBox3.getSelectedItem();
             String toDay1 = (String) jComboBox1.getSelectedItem();
             
             int fromYear = Integer.parseInt(fromYear1);
             int fromMonth = Integer.parseInt(fromMonth1);
             int fromDay = Integer.parseInt(fromDay1);
             int toYear = Integer.parseInt(toYear1);
             int toMonth = Integer.parseInt(toMonth1);
             int toDay = Integer.parseInt(toDay1);
 
            if ((fromYear > toYear) || (fromYear <= toYear && fromMonth > toMonth)
                    || (fromYear <= toYear && fromMonth <= toMonth && fromDay > toDay)) {
                 throw new IllegalArgumentException();
             }
             
             String fromDate = "" + fromYear + "-" + fromMonth + "-" + fromDay;
             String toDate = "" + toYear + "-" + toMonth + "-" + toDay;
             int count = (Integer) jSpinner1.getValue();
             boolean top = false;
             if (jRadioButton1.isSelected()) {
                 top = true;
             }
             ArrayList<String[]> tab = gui.getMenuSalesStats(fromDate, toDate, count, top);
             String description;
             if (top) {
                 description = "Top";
             } else {
                 description ="Least";
             }
             description += " " + count + " selling menus for interval " + fromDate + " - " + toDate;
             PieChart pi = new PieChart("StreamFishâ„¢", description, gui, tab, 0);
             pi.pack();
             pi.setVisible(true);
             this.dispose();
         } catch (IllegalArgumentException e) {
             showMessageDialog(null, "From date cannot be further ahead in time than to date.", "Chartcreator", ERROR_MESSAGE);
         }
     }//GEN-LAST:event_jButton1ActionPerformed
