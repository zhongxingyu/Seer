 package com.git;
 
 /**
 * 人员信息
  * @author stphenZKang
  */
 public class Person {
 	private String name;
 	
 	private String email;
 	
 	private boolean sex;
 
 	public String getName() {
 		return name;
 	}
 
 	public void setName(String name) {
 		this.name = name;
 	}
 
 	public String getEmail() {
 		return email;
 	}
 
 	public void setEmail(String email) {
 		this.email = email;
 	}
 
 	public boolean isSex() {
 		return sex;
 	}
 
 	public void setSex(boolean sex) {
 		this.sex = sex;
 	}
 	
 	
 }
