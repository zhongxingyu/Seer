 public class DetailActivity extends SherlockFragmentActivity implements ServerInteractionResponseInterface {
     public static final String URL = "com.whiterabbit.appennino.url";
     public static final String TITLE = "com.whiterabbit.appennino.title";
     public static final String LAST_UPDATE = "com.whiterabbit.appennino.lastupdate";
     public static final String WEBCAM_ID = "com.whiterabbit.appennino.webcamid";
 
     private String mUrl;
     private String mTitle;
     private long mWebcamId;
     private boolean mUpdating;
     private long mLastUpdate;
 
     private WebcamDetailFragment mDetailFragment;
     private ServerInteractionHelper mServerHelper;
 
     @Override
     protected void onCreate(Bundle savedInstanceState){
         super.onCreate(savedInstanceState);
         requestWindowFeature(Window.FEATURE_INDETERMINATE_PROGRESS);
         setContentView(R.layout.webcam_activity);
 
         Intent i = getIntent();
         mUrl = i.getStringExtra(URL);
         mTitle = i.getStringExtra(TITLE);
         mLastUpdate = i.getLongExtra(LAST_UPDATE, 0);
         mWebcamId = i.getLongExtra(WEBCAM_ID, 0);
 
 
         mServerHelper = ServerInteractionHelper.getInstance(this);
         mDetailFragment = (WebcamDetailFragment) getSupportFragmentManager().findFragmentById(R.id.webcam_detail_fragment);
 
         mDetailFragment.update(mTitle, mUrl, mWebcamId);
     }
 
     @Override
     protected void onResume() {
         super.onResume();    // Autogenerated
         mServerHelper.registerEventListener(this, this);
         if(mServerHelper.isRequestAlreadyPending(mUrl)){
             setUpdating();
         }
     }
 
     @Override
     public void onServerResult(String result, String requestId) {
         if(requestId.equals(mUrl)){
             mDetailFragment.update(mTitle, mUrl, mWebcamId);
             setSupportProgressBarIndeterminateVisibility(false);
             updateDone();
         }
     }
 
     @Override
     public void onServerError(String result, String requestId) {
         setSupportProgressBarIndeterminateVisibility(false);
         updateDone();
         Toast toast = Toast.makeText(this, getString(R.string.download_error), Toast.LENGTH_SHORT);
         toast.show();
     }
 
     @Override
     protected void onPause() {
         super.onPause();
         mServerHelper.unregisterEventListener(this, this);
     }
 
     @Override
     public boolean onMenuItemSelected(int featureId, MenuItem item) {
         if(item.getTitle().equals("Refresh")){
             try {
                 mServerHelper.sendRestAction(this, mUrl, new WebcamDownloadAction(mUrl, mWebcamId));
                 setUpdating();
                 return true;
             } catch (SendingCommandException e) {
                 // TODO Handle failure
             }
         }
         return false;
     }
 
     @Override
     public boolean onCreateOptionsMenu(Menu menu) {
         if(!mUpdating){
             menu.add("Refresh")
                 .setIcon(R.drawable.ic_refresh)
                 .setShowAsAction(MenuItem.SHOW_AS_ACTION_IF_ROOM | MenuItem.SHOW_AS_ACTION_WITH_TEXT);
             return true;
         }
         return false;
 
     }
 
 
     private void setUpdating(){
         mUpdating = true;
         setSupportProgressBarIndeterminateVisibility(true);
         invalidateOptionsMenu();
     }
 
     private void updateDone(){
         mUpdating = false;
         setSupportProgressBarIndeterminateVisibility(false);
         invalidateOptionsMenu();
     }
 
 
 
 }
