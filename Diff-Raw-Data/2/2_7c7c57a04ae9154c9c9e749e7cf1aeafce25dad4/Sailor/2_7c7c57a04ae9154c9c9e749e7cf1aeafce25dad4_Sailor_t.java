 package library;
 
 public class Sailor {
     /*
      * This useful for computing area of a figure with straight lines
      * Requirements:
     *     	The lines in an order
      *
      * Algorithm:
      *
      * Go through the lines in an order.
      * if it horizontal, take the y-coordinate times the length of the
      * line (area under/above the line)
      * if you are going left, multiply the area by -1
      * sum up all the areas.
      * take the absolute value
      */
 
     /*
      * Area of polygon
      * A= 1/2 * sum ( y(i+1) * x(i) - y(i) * x(i+1)
      */
 
     /*
      * Computation of centroid of polygon
      * C(x) = 1 / ( 6 * A ) * sum ( x(i) + x(i+1) ) * ( x(i) * y(i+1) - x(i+1) * y(i) )
      * C(y) = 1 / ( 6 * A ) * sum ( y(i) + y(i+1) ) * ( x(i) * y(i+1) - x(i+1) * y(i) )
      */
 }
