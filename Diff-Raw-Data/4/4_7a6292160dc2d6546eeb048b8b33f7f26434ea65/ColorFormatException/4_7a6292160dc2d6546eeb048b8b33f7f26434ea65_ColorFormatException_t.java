 package glui;
 
@SuppressWarnings("serial")
 public class ColorFormatException extends GLUIException {
 
 	public ColorFormatException() {
 		super();
 	}
 
 	public ColorFormatException(String message, Throwable cause,
 			boolean enableSuppression, boolean writableStackTrace) {
 		super(message, cause, enableSuppression, writableStackTrace);
 	}
 
 	public ColorFormatException(String message, Throwable cause) {
 		super(message, cause);
 	}
 
 	public ColorFormatException(String message) {
 		super(message);
 	}
 
 	public ColorFormatException(Throwable cause) {
 		super(cause);
 	}
 	
 }
