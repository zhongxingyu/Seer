     public void update(Observable o, Object arg) {
         try {
             if (progressInput instanceof InputStream) {
                 progressBar.setValue(progressBar.getMaximum()
                     - ((InputStream)progressInput).available());
             }
         } catch (Exception ex) {
             ex.printStackTrace();
             o.deleteObserver(this);
         }
     }
     
